package Day002;

public class floatDemo {
    public static void main(String[] args) {
        //flaot类型末尾带f  double类型不带f
        float f = 1234.328f;
        System.out.println(f);
        double d = 5623.464;
        System.out.println(d);
        //d1 将整形赋值给浮点型
        double d1 = 123;
        System.out.println(d1);
        //变量间的赋值
        double d2 = d;
        System.out.println("d2 =" + d2  );

    }
}
